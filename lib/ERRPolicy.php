	private static function getValue( $tld, $idx, $default ) {
		if ( 1 < $idx ) {
			return $default;
		}
		if ( ! array_key_exists( $tld, self::$errp ) ) {
			return $default;
		}
		if ( ! is_array( self::$errp[ $tld ] ) ) {
			return $default;
		}
		if ( 2 !== count( self::$errp[ $tld ] ) ) {
			return $default;
		}

		return (int) self::$errp[ $tld ][ $idx ];
	}

